package ui

// Rect defines a rectangle by the coordinates of lower-left and upper-right corners.
type Rect struct {
	// Left is x-coordinate of lower-left corner of the rectangle.
	Left int
	// Bottom is y-coordinate of lower-left corner of the rectange.
	Bottom int
	// Right is x-coordinate of upper-right corner of the rectangle.
	Right int
	// Top is y-coordinate of upper-right corner of the rectangle.
	Top int
}
